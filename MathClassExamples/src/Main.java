public class Main {
    public static void main(String[] args) {
        // Math.ceil(x) >> yuvarlama üste
        // Math.cos(x) >> cos alır
        // Math.sin(x) >> sin alır
        // Math.floor(x) >> yuvarlama alta
        // Math.max(x,y) >> büyük olan sayıyı verir
        // Math.min(x,y) >> küçük olan sayıyı verir
        // Math.pow(3,5) >> 3^5 üs alır
        // Math.random() >> 1 ile 0 arası rastgele değer
        // Math.round(x) >> yakın olan tam sayıya döner
        // Math.abs(x) >> mutlak değerini alır
        // Math.sqrt(16) >> karesini alır

        System.out.println(Math.ceil(1.1));
        System.out.println(Math.floor(1.7));
        System.out.println(Math.cos(30));
        System.out.println(Math.max(1.6, 3));
        System.out.println(Math.round(1.5));
        System.out.println(Math.abs(-1.6));
        System.out.println(Math.sqrt(1.6));


    }
}