public class ExecuteOperations {
    public int add (int num1, int num2) {
        return num1 + num2;
    }
    public int subs (int num1, int num2) {
        return num1 - num2;
    }
    public int multiply (int num1, int num2) {
        return num1 * num2;
    }
    public int div (int num1, int num2) {
        return num1 / num2;
    }
}
